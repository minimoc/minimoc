struct CarouselIcon {
  const char*          label;
  const unsigned char* bmp16;   // 16x16 bitmap (PROGMEM)
  const unsigned char* bmp32;   // 32x32 bitmap (PROGMEM)
};

// Generic draw helper — call this to render any icon at any size/position.
// size must be 16 or 32. Uses U8g2 drawXBMP (LSB-first, PROGMEM-safe).
void drawIcon(int x, int y, int size, const unsigned char* bmp) {
  u8g2.drawXBMP(x, y, size, size, bmp);
}

// -----------------------------------------------------------------
// CAROUSEL STATE
// -----------------------------------------------------------------

#define CAROUSEL_ITEMS_MAX 8

struct CarouselItem {
  const char*          label;
  const unsigned char* bmp16;
  const unsigned char* bmp32;
  void (*onSelect)();   // called when user validates this item
};

static CarouselItem  carousel_items[CAROUSEL_ITEMS_MAX];
static uint8_t       carousel_count     = 0;
static int           carousel_selected  = 0;

// Fractional pixel offset for smooth scroll animation (fixed-point, 256 = 1 slot)
static int16_t       carousel_anim_offset = 0;  // 0 = settled
static const int8_t  ANIM_STEP = 16; // pixels per frame to slide

// -----------------------------------------------------------------
// CAROUSEL SETUP — call once during setup() after menu init
// -----------------------------------------------------------------

void carousel_register(const char* label,
                       const unsigned char* bmp16,
                       const unsigned char* bmp32,
                       void (*onSelect)()) {
  if (carousel_count >= CAROUSEL_ITEMS_MAX) return;
  carousel_items[carousel_count++] = {label, bmp16, bmp32, onSelect};
}

// -----------------------------------------------------------------
// DRAW HELPERS
// -----------------------------------------------------------------

// Screen constants (SSD1306 128x64)
#define SCREEN_W  128
#define SCREEN_H  64
#define ICON_BIG   32
#define ICON_SMALL 16
#define LABEL_Y    58   // baseline for bottom label text

// -----------------------------------------------------------------
// GLOBAL FONT SCALE — change these three lines to resize all sub-menu text
#define UI_FONT_TITLE  u8g2_font_7x13_tf   // section headers  (7×13 px)
#define UI_FONT_BODY   u8g2_font_6x10_tf   // list items / body text (6×10 px)
#define UI_FONT_SMALL  u8g2_font_5x7_tf    // tight areas: grids, slot labels, badges (5×7 px)
// -----------------------------------------------------------------

// 5-icon layout — total: 16+4+16+4+32+4+16+4+16 = 112px → 8px margin each side
#define ICON_FAR_LEFT_X    8
#define ICON_NEAR_LEFT_X  28
#define CENTER_X          48
#define ICON_NEAR_RIGHT_X 84
#define ICON_FAR_RIGHT_X 104

// Y positions
#define BIG_ICON_Y   ((48 - ICON_BIG)   / 2)   // = 8
#define SMALL_ICON_Y ((48 - ICON_SMALL) / 2)   // = 16

// Animation: pixel distance from center slot to adjacent near slot
#define CAROUSEL_SLOT_OFFSET  36

// -----------------------------------------------------------------
// CAROUSEL DRAW — renders one complete frame into U8g2 buffer
// offset_px: pixel offset of the center slot (-ve = sliding left,
//            +ve = sliding right). 0 = fully settled.
// -----------------------------------------------------------------

void carousel_draw_frame(int offset_px) {
  u8g2.clearBuffer();

  int n = carousel_count;
  if (n == 0) { u8g2.sendBuffer(); return; }

  // 5 visible slots — circular indices
  int sel   = carousel_selected;
  int prev1 = (sel + n - 1) % n;
  int prev2 = (sel + n - 2) % n;
  int next1 = (sel + 1) % n;
  int next2 = (sel + 2) % n;

  //drawIcon(ICON_FAR_LEFT_X   + offset_px, SMALL_ICON_Y, ICON_SMALL, carousel_items[prev2].bmp16);
  //drawIcon(ICON_NEAR_LEFT_X  + offset_px, SMALL_ICON_Y, ICON_SMALL, carousel_items[prev1].bmp16);
  drawIcon(CENTER_X          + offset_px, BIG_ICON_Y,   ICON_BIG,   carousel_items[sel].bmp32);
  //drawIcon(ICON_NEAR_RIGHT_X + offset_px, SMALL_ICON_Y, ICON_SMALL, carousel_items[next1].bmp16);
  //drawIcon(ICON_FAR_RIGHT_X  + offset_px, SMALL_ICON_Y, ICON_SMALL, carousel_items[next2].bmp16);

  // Label centered, larger font
  u8g2.setFont(u8g2_font_8x13_tf);
  const char* lbl = carousel_items[sel].label;
  int lbl_w = u8g2.getStrWidth(lbl);
  u8g2.drawStr((SCREEN_W - lbl_w) / 2, LABEL_Y, lbl);

  u8g2.sendBuffer();
}

// -----------------------------------------------------------------
// CAROUSEL UPDATE — call every ~50 ms from the interactive loop
// Handles encoder input and animation ticking.
// Returns true if user just validated a selection.
// -----------------------------------------------------------------

bool carousel_update(bool enc_up, bool enc_down, bool btn_valid) {

  // Animate towards zero offset
  if (carousel_anim_offset != 0) {
    if (carousel_anim_offset > 0) {
      carousel_anim_offset -= ANIM_STEP;
      if (carousel_anim_offset < 0) carousel_anim_offset = 0;
    } else {
      carousel_anim_offset += ANIM_STEP;
      if (carousel_anim_offset > 0) carousel_anim_offset = 0;
    }
    carousel_draw_frame(carousel_anim_offset);
    return false;
  }

  // Only accept new input when fully settled
  int n = carousel_count;
  if (n == 0) return false;

  if (enc_down) {
    carousel_selected = (carousel_selected + 1) % n;
    carousel_anim_offset = +CAROUSEL_SLOT_OFFSET;  // slide right→left
  } else if (enc_up) {
    carousel_selected = (carousel_selected + n - 1) % n;
    carousel_anim_offset = -CAROUSEL_SLOT_OFFSET;  // slide left→right
  }

  if (btn_valid) {
    if (carousel_items[carousel_selected].onSelect != NULL) {
      carousel_items[carousel_selected].onSelect();
    }
    return true;
  }

  carousel_draw_frame(carousel_anim_offset);
  return false;
}

// -----------------------------------------------------------------
// SOURCE-PORT REMINDER — draws a small "Port X" badge at the top-
// left of the screen. Call this inside sub-menu draw functions when
// you want to keep the context visible.
// -----------------------------------------------------------------

void carousel_draw_port_badge(int port_index) {
  static const char* PORT_LABELS[] = { "IN:A", "IN:B", "IN:C", "IN:D", "IN:E" };
  u8g2.setFont(UI_FONT_SMALL);
  u8g2.drawRFrame(0, 0, 24, 10, 2);
  u8g2.drawStr(3, 8, PORT_LABELS[port_index < 5 ? port_index : 0]);
}

// -----------------------------------------------------------------
// OUTPUT LABEL HELPERS
// Short codes: Sorties 1-9 → "1".."9"
// Used in sub-menu rows.
// -----------------------------------------------------------------

const char* carousel_output_label(uint8_t output_idx) {
  static char buf[4];
  snprintf(buf, sizeof(buf), "%u", output_idx + 1);
  return buf;
}

const char* carousel_channel_label(uint8_t ch) {
  // ch: 1-16
  static char buf[4];
  if (ch == 0) {
    return "OFF";
  }
  snprintf(buf, sizeof(buf), "%u", ch);
  return buf;
}
